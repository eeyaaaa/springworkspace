package survey;

//응답자의 정보를 담기위한 클래스 
public class Respondent {
	private int age;
	private String location;
	
	public int getAge() {
		return age;
	}
	public String getLocation() {
		return location;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	
	
}
